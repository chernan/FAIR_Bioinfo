#-------------------------------------------------------------------------------
# Get arguments
#-------------------------------------------------------------------------------

args <- commandArgs(TRUE)

#-------------------------------------------------------------------------------
# Get name
#-------------------------------------------------------------------------------

nameCondition <- unlist(strsplit(args[1],"/"))[2]
nameCondition <- unlist(strsplit(args[1],"[.]"))[1]
nameCondition <- unlist(strsplit(nameCondition,"_"))[2]

#-------------------------------------------------------------------------------
# Read data
#-------------------------------------------------------------------------------

df <- read.table(args[1], header=F)

#-------------------------------------------------------------------------------
# Graphical representation
#-------------------------------------------------------------------------------

pdf(paste0("graphics/graphics-",nameCondition,".pdf"))
par(mfrow=c(1,2))
boxplot(df[,2], outline=T, main = "With outliers", ylab='# of reads by ORF', xlab= args[1])
boxplot(df[,2], outline=F, main = "Without outliers", ylab='# of reads by ORF', xlab= args[1])
dev.off()
